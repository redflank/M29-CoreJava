// Program to demonstrate on various DataTypes
package org.tnsif.introduction;
public class DataTypesDemo {

	public static void main(String[] args) {
		//integer will give us the values in whole number
		//float and double will give us values in decimal point
		int num1=5/2;
		float num2=103/45;
		double num3=10d/3d;
		float num4=14f/3f;
		
		System.out.println("The value of num1: "+num1);
		System.out.println("The value of num2: "+num2);
		System.out.println("The value of num3: "+num3);
		System.out.println("The value of num4: "+num4);
	}

}
